class Employee:
    def __init__(self, id, name, birthdate, role, mail, hoursType, baseSalary, comissionRate):
        self.id = id
        self.name = name
        self.birthdate = birthdate
        self.role = role
        self.mail = mail
        self.hoursType = hoursType
        self.baseSalary = baseSalary
        self.comissionRate = comissionRate